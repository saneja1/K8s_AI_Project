    "# 🤖 Multi-Agent System with Supervisor Pattern\n",
    "\n",
    "## 📚 Developer Guide: How to Build Similar Multi-Agent Systems\n",
    "\n",
    "### 🎯 What Makes This Code Powerful?\n",
    "\n",
    "This notebook demonstrates how to create a **production-ready multi-agent system** with just ~50 lines of code.\n",
    "The magic comes from using `langgraph-supervisor` - a pre-built package that handles all the complexity\n",
    "of agent routing, state management, and inter-agent communication.\n",
    "\n",
    "---\n",
    "\n",
    "## 🏗️ Architecture Pattern\n",
    "\n",
    "```\n",
    "USER QUESTION\n",
    "     ↓\n",
    "SUPERVISOR (Router/Manager)\n",
    "     ↓\n",
    "     ├─→ RESEARCH AGENT (Web Search Expert) → transfer_back_to_supervisor\n",
    "     ├─→ MATH AGENT (Calculation Expert)    → transfer_back_to_supervisor\n",
    "     └─→ [Add more agents here...]\n",
    "     ↓\n",
    "FINAL ANSWER (Supervisor synthesizes results)\n",
    "```\n",
    "\n",
    "**Key Concept:** The supervisor acts as a smart router that:\n",
    "1. Analyzes the user's question\n",
    "2. Decides which specialist agent(s) to call\n",
    "3. Executes agents sequentially or in parallel\n",
    "4. Collects all results and synthesizes final answer\n",
    "\n",
    "---\n",
    "\n",
    "## 🚀 Step-by-Step Guide to Replicate\n",
    "\n",
    "### **STEP 1: Define Your Tools (Simple Python Functions)**\n",
    "\n",
    "Tools are just regular Python functions with **docstrings**. The docstring tells the AI what the tool does.\n",
    "\n",
    "```python\n",
    "def your_custom_tool(param1: str, param2: int) -> str:\n",
    "    '''Brief description of what this tool does. AI reads this to decide when to use it.'''\n",
    "    # Your implementation here\n",
    "    return result\n",
    "```\n",
    "\n",
    "**Rules:**\n",
    "- Use **type hints** (str, int, float, etc.) - helps AI understand parameters\n",
    "- Write **clear docstrings** - this is how AI knows what the tool does\n",
    "- Keep tools **focused** - one tool should do ONE thing well\n",
    "\n",
    "---\n",
    "\n",
    "### **STEP 2: Create Specialized Agents (3 Lines Each!)**\n",
    "\n",
    "Use `create_react_agent()` from `langgraph.prebuilt` to create specialist agents.\n",
    "\n",
    "```python\n",
    "from langgraph.prebuilt import create_react_agent\n",
    "\n",
    "specialist_agent = create_react_agent(\n",
    "    model=your_llm_model,              # GPT-4, Claude, Gemini, etc.\n",
    "    tools=[tool1, tool2, tool3],       # List of tools this agent can use\n",
    "    name=\"agent_name\",                 # Unique identifier (used for routing)\n",
    "    prompt=\"Clear role definition\"     # What this agent is expert in\n",
    ")\n",
    "```\n",
    "\n",
    "**Best Practices:**\n",
    "- **Name:** Use descriptive names like `research_expert`, `math_expert`, `k8s_node_expert`\n",
    "- **Tools:** Only give tools relevant to this agent's domain\n",
    "- **Prompt:** Be specific! \"You are X expert. Always do Y. Never do Z.\"\n",
    "- **Constraints:** Add rules like \"Always use one tool at a time\" to prevent errors\n",
    "\n",
    "---\n",
    "\n",
    "### **STEP 3: Create the Supervisor (The Magic Router)**\n",
    "\n",
    "Use `create_supervisor()` to manage all your specialist agents.\n",
    "\n",
    "```python\n",
    "from langgraph_supervisor import create_supervisor\n",
    "\n",
    "workflow = create_supervisor(\n",
    "    [agent1, agent2, agent3],          # List of all specialist agents\n",
    "    model=your_llm_model,              # Same model or different one\n",
    "    prompt=\"\n",
    "        You are a team supervisor managing multiple experts.\n",
    "        \n",
    "        Routing Rules:\n",
    "        - For questions about X, use agent1\n",
    "        - For questions about Y, use agent2\n",
    "        - For complex questions, use multiple agents sequentially\n",
    "        \n",
    "        Always synthesize agent responses into a clear final answer.\n",
    "    \"\n",
    ")\n",
    "```\n",
    "\n",
    "**Supervisor Prompt Tips:**\n",
    "- Be **explicit** about routing rules\n",
    "- Specify when to use **multiple agents** vs single agent\n",
    "- Tell it to **synthesize** results, not just pass them through\n",
    "\n",
    "---\n",
    "\n",
    "### **STEP 4: Compile & Execute**\n",
    "\n",
    "```python\n",
    "# Compile the workflow (one-time setup)\n",
    "app = workflow.compile()\n",
    "\n",
    "# Execute with user question\n",
    "result = app.invoke({\n",
    "    \"messages\": [\n",
    "        {\"role\": \"user\", \"content\": \"Your question here\"}\n",
    "    ]\n",
    "})\n",
    "\n",
    "# Access results\n",
    "for message in result[\"messages\"]:\n",
    "    message.pretty_print()  # Shows full conversation flow\n",
    "```\n",
    "\n",
    "---\n",
    "\n",
    "## 🎓 How Agent Transfer Works (Automatic!)\n",
    "\n",
    "When you create a supervisor, it **automatically generates transfer tools**:\n",
    "\n",
    "1. `transfer_to_research_expert()` - Routes to research agent\n",
    "2. `transfer_to_math_expert()` - Routes to math agent\n",
    "3. `transfer_back_to_supervisor()` - Returns control to supervisor\n",
    "\n",
    "**You don't write these!** They're created based on agent names.\n",
    "\n",
    "**Execution Flow:**\n",
    "```\n",
    "User: \"What's the weather in Delhi? Multiply by 2.\"\n",
    "  ↓\n",
    "Supervisor decides: \"Need research first\"\n",
    "  ↓\n",
    "Calls: transfer_to_research_expert()\n",
    "  ↓\n",
    "Research Agent: Uses search_duckduckgo() → \"13°C\"\n",
    "  ↓\n",
    "Calls: transfer_back_to_supervisor()\n",
    "  ↓\n",
    "Supervisor decides: \"Now need math\"\n",
    "  ↓\n",
    "Calls: transfer_to_math_expert()\n",
    "  ↓\n",
    "Math Agent: Uses multiply(13, 2) → \"26\"\n",
    "  ↓\n",
    "Calls: transfer_back_to_supervisor()\n",
    "  ↓\n",
    "Supervisor: \"Weather is 13°C, multiplied by 2 equals 26\"\n",
    "```\n",
    "\n",
    "---\n",
    "\n",
    "## 💡 Real-World Example: Kubernetes Multi-Agent System\n",
    "\n",
    "```python\n",
    "# Tools grouped by domain\n",
    "node_tools = [check_node_health, get_node_resources, describe_node]\n",
    "pod_tools = [get_pods, get_pod_logs, get_pod_events]\n",
    "monitoring_tools = [get_resource_usage, check_cluster_health]\n",
    "\n",
    "# Create specialized agents\n",
    "node_agent = create_react_agent(\n",
    "    model=llm,\n",
    "    tools=node_tools,\n",
    "    name=\"k8s_node_expert\",\n",
    "    prompt=\"You are a Kubernetes node expert. Handle node health, taints, and resource checks.\"\n",
    ")\n",
    "\n",
    "pod_agent = create_react_agent(\n",
    "    model=llm,\n",
    "    tools=pod_tools,\n",
    "    name=\"k8s_pod_expert\",\n",
    "    prompt=\"You are a Kubernetes pod expert. Handle pod status, logs, and troubleshooting.\"\n",
    ")\n",
    "\n",
    "monitor_agent = create_react_agent(\n",
    "    model=llm,\n",
    "    tools=monitoring_tools,\n",
    "    name=\"k8s_monitor_expert\",\n",
    "    prompt=\"You are a Kubernetes monitoring expert. Check resource usage and cluster health.\"\n",
    ")\n",
    "\n",
    "# Create supervisor\n",
    "supervisor = create_supervisor(\n",
    "    [node_agent, pod_agent, monitor_agent],\n",
    "    model=llm,\n",
    "    prompt=\"\n",
    "        You manage Kubernetes experts:\n",
    "        - For node questions (health, taints) → k8s_node_expert\n",
    "        - For pod questions (logs, status) → k8s_pod_expert  \n",
    "        - For cluster-wide metrics → k8s_monitor_expert\n",
    "        \n",
    "        For complex questions, use multiple agents sequentially.\n",
    "    \"\n",
    ")\n",
    "\n",
    "app = supervisor.compile()\n",
    "```\n",
    "\n",
    "---\n",
    "\n",
    "## ✅ Advantages Over Single-Agent Systems\n",
    "\n",
    "| Aspect | Single Agent | Multi-Agent Supervisor |\n",
    "|--------|-------------|------------------------|\n",
    "| **Tool Management** | All tools in one agent (10-20+) | Organized by domain (3-5 per agent) |\n",
    "| **Prompt Clarity** | One prompt does everything | Focused prompts per expert |\n",
    "| **Parallel Execution** | Sequential only | Can execute agents in parallel |\n",
    "| **Error Handling** | Global try-catch | Per-agent error handling |\n",
    "| **Scalability** | Degrades with more tools | Add agents without affecting others |\n",
    "| **Accuracy** | Confused with too many tools | Clear routing = better tool selection |\n",
    "\n",
    "---\n",
    "\n",
    "## ⚠️ Important Notes\n",
    "\n",
    "1. **Agent Naming Convention:** Use descriptive suffixes like `_expert`, `_agent`, `_specialist`\n",
    "2. **Tool Overlap:** Avoid giving the same tool to multiple agents (confuses routing)\n",
    "3. **Prompt Engineering:** The supervisor prompt is CRITICAL - be very explicit about routing\n",
    "4. **Sequential Constraints:** Add \"Always use one tool at a time\" to prevent parallel errors\n",
    "5. **Testing:** Test with questions that require single agent vs multiple agents\n",
    "\n",
    "---\n",
    "\n",
    "## 📦 Required Dependencies\n",
